const HARDCOVER_API = 'https://api.hardcover.app/v1/graphql';
const yaml = require('js-yaml');
const fs = require('fs');
const path = require('path');

const query = `{
  me {
    user_books(where: {status_id: {_eq: 2}}) {
      first_started_reading_date
      date_added
      book {
        title
        slug
        cached_contributors
      }
    }
  }
}`;

async function main() {
    const apiKey = process.env.HARDCOVER_API_KEY;
    if (!apiKey) {
        console.error('Error: HARDCOVER_API_KEY environment variable is required');
        process.exit(1);
    }

    console.log('Fetching currently reading books from Hardcover...');

    const response = await fetch(HARDCOVER_API, {
        method: 'POST',
        headers: {
            'content-type': 'application/json',
            authorization: apiKey.startsWith('Bearer ') ? apiKey : `Bearer ${apiKey}`,
        },
        body: JSON.stringify({ query }),
    });

    if (!response.ok) {
        console.error(`API request failed: ${response.status} ${response.statusText}`);
        process.exit(1);
    }

    const { data, errors } = await response.json();

    if (errors) {
        console.error('GraphQL errors:', JSON.stringify(errors, null, 2));
        process.exit(1);
    }

    const userBooks = data?.me?.[0]?.user_books || [];
    console.log(`Found ${userBooks.length} currently reading book(s)`);

    const books = [];

    for (const ub of userBooks) {
        const book = ub.book;
        const author = book.cached_contributors?.[0]?.author?.name
            || book.cached_contributors?.[0]?.name
            || 'Unknown';

        const entry = {
            title: book.title,
            author: author,
            hardcover_url: 'https://hardcover.app/books/' + book.slug,
        };

        if (ub.first_started_reading_date || ub.date_added) {
            entry.started_at = (ub.first_started_reading_date || ub.date_added).split('T')[0];
        }

        // Attach last_updated to the first entry so Hugo can display it
        if (ub === userBooks[0]) {
            entry.last_updated = new Date().toISOString().split('T')[0];
        }

        books.push(entry);
    }

    let yamlContent = '# Auto-generated by scripts/fetch-hardcover.js — do not edit manually\n';

    if (books.length === 0) {
        yamlContent += '# No books currently being read\n';
    } else {
        // use js-yaml to generate safe YAML output
        yamlContent += '\n' + yaml.dump(books, {
            quotingType: '"',
            forceQuotes: true,
        });
    }

    const outPath = path.join(__dirname, '..', 'data', 'activity', 'currently_reading.yaml');
    fs.writeFileSync(outPath, yamlContent);
    console.log(`Wrote ${outPath}`);
}

main().catch(err => {
    console.error(err);
    process.exit(1);
});
